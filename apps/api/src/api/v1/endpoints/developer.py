# ========================== Agent OS Workflows ==========================

@router.get("/workflows/templates")
async def list_workflow_templates():
    """List available Agent OS workflow templates."""
    return {"templates": workflow_executor.list_templates()}


@router.get("/workflows/templates/{template_id}")
async def get_workflow_template(template_id: str):
    """Get a workflow template definition."""
    t = workflow_executor.get_template(template_id)
    if not t:
        raise HTTPException(status_code=404, detail="Template not found")
    return {
        "id": t.id, "name": t.name, "description": t.description,
        "version": t.version, "tags": t.tags,
        "steps": [
            {"id": s.id, "name": s.name, "agent": s.agent, "action": s.action,
             "depends_on": s.depends_on, "params": s.params}
            for s in t.steps
        ],
    }


class StartWorkflowRequest(BaseModel):
    template_id: str
    context: Optional[Dict[str, Any]] = None


@router.post("/workflows/run")
async def start_workflow(
    body: StartWorkflowRequest,
):
    """Start executing an Agent OS workflow."""
    try:
        run = await workflow_executor.start_workflow(body.template_id, body.context)
    except ValueError as e:
        raise HTTPException(status_code=404, detail=str(e))
    return {
        "run_id": run.id,
        "template_id": run.template_id,
        "status": run.status,
        "steps_completed": run.steps_completed,
        "steps_failed": run.steps_failed,
        "results": run.results,
        "duration_seconds": round((run.completed_at or 0) - run.started_at, 2),
    }


@router.get("/workflows/runs")
async def list_workflow_runs(limit: int = Query(50, ge=1, le=200)):
    """List recent workflow runs."""
    return {"runs": workflow_executor.list_runs(limit=limit)}


@router.get("/workflows/runs/{run_id}")
async def get_workflow_run(run_id: str):
    """Get a workflow run status."""
    run = workflow_executor.get_run(run_id)
    if not run:
        raise HTTPException(status_code=404, detail="Workflow run not found")
    return {
        "id": run.id, "template_id": run.template_id, "status": run.status,
        "steps_completed": run.steps_completed, "steps_failed": run.steps_failed,
        "context": run.context, "results": run.results,
        "started_at": run.started_at, "completed_at": run.completed_at,
    }


# ========================== HITL Approvals ==========================

@router.get("/workflows/approvals/pending")
async def list_pending_approvals(
    workflow_run_id: Optional[str] = Query(None, description="Filter by workflow run ID"),
):
    """List pending human-in-the-loop approval requests."""
    pending = approval_gate.list_pending(workflow_run_id=workflow_run_id)
    return {"pending": pending}


class DecideApprovalRequest(BaseModel):
    decision: str = Field(..., description="approved | rejected | modified")
    reason: str = Field("", description="Optional reason for the decision")
    modifications: Optional[Dict[str, Any]] = Field(None, description="Optional payload modifications when decision=modified")
    decided_by: str = Field("human", description="Identifier of the decider")


@router.post("/workflows/approvals/{gate_id}/decide")
async def decide_approval(gate_id: str, body: DecideApprovalRequest):
    """Approve, reject, or modify a pending approval gate."""
    if body.decision not in ("approved", "rejected", "modified"):
        raise HTTPException(status_code=400, detail="decision must be approved, rejected, or modified")
    ok = await approval_gate.decide(
        gate_id,
        decision=body.decision,
        decided_by=body.decided_by,
        reason=body.reason,
        modifications=body.modifications,
    )
    if not ok:
        raise HTTPException(status_code=404, detail="Approval gate not found")
    return {"status": "ok", "gate_id": gate_id}
