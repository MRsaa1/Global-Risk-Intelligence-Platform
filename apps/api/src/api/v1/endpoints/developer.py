router = APIRouter()


# ========================== Webhooks ==========================

class WebhookCreate(BaseModel):
    name: str = Field(..., min_length=1, max_length=100)
    url: str = Field(..., min_length=10)
    events: List[str] = Field(default_factory=lambda: ["alert.created", "stress_test.completed"])


class WebhookResponse(BaseModel):
    id: str
    name: str
    url: str
    events: List[str]
    is_active: bool
    secret: Optional[str] = None
    created_at: Optional[str] = None


@router.post("/webhooks", response_model=WebhookResponse)
async def create_webhook(
    body: WebhookCreate,
    user: User = Depends(get_current_active_user),
    db: AsyncSession = Depends(get_db),
):
    """Create a webhook subscription."""
    secret = secrets.token_urlsafe(32)
    wh = Webhook(
        id=str(uuid4()),
        user_id=user.id,
        name=body.name,
        url=body.url,
        secret=secret,
        events=json.dumps(body.events),
    )
    db.add(wh)
    await db.commit()
    return WebhookResponse(
        id=wh.id, name=wh.name, url=wh.url,
        events=body.events, is_active=True, secret=secret,
        created_at=wh.created_at.isoformat() if wh.created_at else None,
    )


@router.get("/webhooks")
async def list_webhooks(
    user: User = Depends(get_current_active_user),
    db: AsyncSession = Depends(get_db),
):
    """List webhooks for current user."""
    result = await db.execute(
        select(Webhook).where(Webhook.user_id == user.id).order_by(Webhook.created_at.desc())
    )
    hooks = result.scalars().all()
    return [
        {
            "id": h.id, "name": h.name, "url": h.url,
            "events": json.loads(h.events) if h.events else [],
            "is_active": h.is_active,
            "failure_count": h.failure_count,
            "last_triggered_at": h.last_triggered_at.isoformat() if h.last_triggered_at else None,
        }
        for h in hooks
    ]


@router.delete("/webhooks/{webhook_id}")
async def delete_webhook(
    webhook_id: str,
    user: User = Depends(get_current_active_user),
    db: AsyncSession = Depends(get_db),
):
    """Delete a webhook."""
    result = await db.execute(
        select(Webhook).where(Webhook.id == webhook_id, Webhook.user_id == user.id)
    )
    wh = result.scalar_one_or_none()
    if not wh:
        raise HTTPException(status_code=404, detail="Webhook not found")
    await db.delete(wh)
    await db.commit()
    return {"status": "deleted", "id": webhook_id}


